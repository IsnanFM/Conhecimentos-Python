#===================================OPERADORES DE COMPARAÇÃO==========================

#OS OPERADORES DE COMPARAÇÃO RETORNAM TRUE OU FALSE (BOOLEANO)

#-------------------------------------IGUALDADE---------------------------------------
#O operador de igualdade comparam dois valores e verifica se são iguais e é simbolizado
#por "=="

var = (1==2)
print(var)

#ou

print(1==2)
print(2==2) 

#output
#False
#False
#True

#-------------------------------------DIFERENÇA----------------------------------------------------------
#Oposto da igualdade e é simbolizado por "!="

print(1!=1)
print(45!=20)

#output
#False
#True
#------------------------MENOR, MENOR ou IGUAL, MAIOR, MAIOR ou IGUAL---------------------------------------
#Tem funções iguais as matemáticas usuais e são simbolizados por "<","<=",">",">="
#EX:

print(2<=2)
print(2<2)

#output
#True
#False

print(2>=2)
print(2>2)

#output
#True
#False

print(32>=2)
print(32<=2)

#output
#True
#False

